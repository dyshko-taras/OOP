package examples.homeworks;

    public class MyMath {
        public static int multiple(int a, int b) {
            return a * b;
        }
        public static double multiple(double a, double b) {
            return a * b;
        }
        public static double multiple(double a) {
            return multiple(a,a);
        }

        public static int multiple(int a) {
            return multiple(a,a);
        }



}
